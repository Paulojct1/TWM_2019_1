    	<br><p id="login" style="font-size: 300%; margin-left: 150px">Sobre</p><br>

    	<br> <br> <br>

        <p id="textbox-text" style="margin-left: 100px; margin-right: 100px; font-size: 180%; line-height: 1.5"> O Oficial de Registro Civil das Pessoas Naturais e Tabelião de Notas de Meridiano iniciou suas atividades em 06 de novembro de 1950, realizando registros de Nascimento, Casamento e Óbito. Também trabalha com reconhecimento de firmas e autenticações, lavratura de Procurações Públicas e Escrituras, como Venda e Compra, Doação, Separações e Divórcios, Inventários e Testamentos, entre outros. </p>

        <br> <br> <br> <br>

        <p id="textbox-text" style="font-size: 220%; margin-left: 150px"> A cidade de Meridiano </p>

        <br> <br> <br>

        <p id="textbox-text" style="margin-left: 100px; margin-right: 100px; font-size: 180%; line-height: 1.5" > É um município brasileiro do estado de São Paulo. Localiza-se a uma latitude 20º21’33” sul e a uma longitude 50º10’24” oeste, estando a uma altitude de 529 metros. Tem uma população de 3.855 habitantes (IBGE/2010) e área de 229,2 km². </p>

        <br> <br>

        <p id="textbox-text" style="margin-left: 100px; margin-right: 100px; font-size: 180%; line-height: 1.5"> Pelos registros existentes, por volta da década de 1940, começava a germinação de mais uma urbe. Chegando por aqui os pioneiros exploradores e aventureiros, homens que na esperança de conseguir um nível econômico mais elevado pela exploração das terras férteis da região, adquiriram glebas no núcleo dos atuais córregos São João, Maravilha, das Pedras e Marinheiro, sendo predominantes, os senhores Ernesto Cavalin e João Machado Pereira, que tiveram em suas áreas o ponto crucial do início da cidade. </p>

        <br> <br> <br> <br>

        <p id="textbox-text" style="font-size: 220%; margin-left: 150px"> Fundação do Povoado </p>

        <br> <br> <br>

        <p id="textbox-text" style="margin-left: 100px; margin-right: 100px; font-size: 180%; line-height: 1.5"> Surgiram os arrendatários e demais pessoas interessadas em fixar residências nestas localidades, acreditando fazer fortuna ou melhora de vida, graças à tendência propícia das terras férteis da região. Nasciam assim, duas povoações com os nomes de: São João da Maravilha e São José da Maravilha. </p>

        <br> <br> <br> <br>

        <p id="textbox-text" style="font-size: 220%; margin-left: 150px"> Criação do Distrito </p>

        <br> <br> <br>

        <p id="textbox-text" style="margin-left: 100px; margin-right: 100px; font-size: 180%; line-height: 1.5"> Esses nomes continuaram até a data em que, em 24 de dezembro de 1948, através do Decreto Lei Estadual nº 233, o povoado foi elevado a categoria de Distrito de Paz, subordinado ao município de Fernandópolis,promulgada pelo Governador Ademar Pereira de Barros, quando então recebeu o nome de MERIDIANO. Conquistada a categoria de distrito, travou-se pelos meridianenses a luta para conseguir a sua emancipação política com vistas a ter a sua própria autonomia, realizar as suas pretensões, sanar a suas deficiências, enfim, desenvolver a sua administração independente. </p>

        <br> <br> <br> <br>

        <p id="textbox-text" style="font-size: 220%; margin-left: 150px"> Criação e emancipação do Município </p>

        <br> <br> <br>

        <p id="textbox-text" style="margin-left: 100px; margin-right: 100px; font-size: 180%; line-height: 1.5"> Graças aos esforços das autoridades constituídas, finalmente se concretizou esse acalentado sonho. Em 18 de fevereiro de 1959, através do Decreto Lei Estadual nº 5.285, Meridiano, foi elevado à categoria de Município, desmembrado do município de Fernandópolis. Categorizado a Município, teve a sua instalação no dia 1º de janeiro de 1960, sendo o seu primeiro Prefeito o senhor Donato Marcelo Balbo, que organizou a instalação dos serviços municipais, e deu início a uma administração cheia de dificuldades, dada a escassez de equipamentos, ferramentas de trabalho e pessoal, mas que tudo dentro de pouco tempo estava superado. O Vice-prefeito daquela Legislatura foi Alderize Zapparoli, e a Câmara de Vereadores foi construída dos seguintes edis: Antônio Ferreira Fontes,  Antônio Pereira Filho,  Carlos Eugênio Masson,  Eliseu Antônio Bueno, Fernando Morandin, Luiz Silvério, Quadreli Luiz Rizzato, Orlando Rizzato e Raul Sóssio Terra. Naquela primeira gestão, Donato Marcelo Balbo, foi praticamente o prefeito que edificou os alicerces do município de Meridiano, adquiriu os primeiros veículos e equipamentos rodoviários para a conservação das estradas municipais, deixou o serviço de abastecimento de água da sede quase concluído, e construiu prédios escolares na zona rural. Com a construção do prédio do Grupo Escolar, reformou totalmente o  prédio do antigo Grupo Escolar, e nele instalou a Prefeitura Municipal, que estava funcionando em local alugado de particular, e incompatível para desempenhar as suas atividades públicas. Energizou toda a cidade, entregando ao seu sucessor um município com reais sintomas de franco progresso. </p>

